public class PrimitiveDataTypes {
    public static void main(String[] args) {
        char c='A';
        float f=12.345f;
        double d=345.87654d;
        short a=111;
        long e=1234;
        boolean t=90<56;
        System.out.println(c);
        System.out.println(f);
        System.out.println(d);
        System.out.println(a);
        System.out.println(e);
        System.out.println(t);

    }
}
